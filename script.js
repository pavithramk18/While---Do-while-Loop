//Sample while program to print 1 to 10 
var n=10;
var i=1;
while(i<n)
{
    console.log(i)
    i++;
}

//Sample while Program to print 100 to 90

var n=100;
var i=90
while(i<n)
{
    console.log(n);
    n--;

}

//Sample do while program to print 1 to 10


var i=0;
do
{
    console.log(i)
    i++;

}while(i<10)
